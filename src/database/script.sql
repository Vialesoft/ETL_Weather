    avghumidity             NUMERIC(5,2),
    daily_will_it_rain      NUMERIC(5,2),
    daily_chance_of_rain    NUMERIC(5,2),
    daily_will_it_snow      NUMERIC(5,2),
    daily_chance_of_snow    NUMERIC(5,2),
