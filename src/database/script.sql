CREATE TABLE Locations(
    load_date   TIMESTAMP default now(),
    update_date TIMESTAMP default now(),
    name        VARCHAR(1000),
    region      VARCHAR(MAX),
    country     VARCHAR(1000),
    latitude    NUMERIC(6,3),
    longitude   NUMERIC(6,3),
    tz_id       VARCHAR(1000),
    localdate   TIMESTAMP
)

